    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        ArrayList listaCorreo = CSV.readCSV("listacorreo.csv");
        boolean estado = false;
        
        for(int i=0; i<listaCorreo.size(); i++){
            Correo correo = new Correo((String) listaCorreo.get(i));
            if(correo.send()){
                estado = true;
            }
        }
        
        if(estado){
            JOptionPane.showMessageDialog(this,"Se Envio Correctamente Los Correos", "Estado Correo", 1);
        }else{
            JOptionPane.showMessageDialog(this,"No Se Pudo Enviar Los Correos", "Estado Correo", 1);
        }
    }//GEN-LAST:event_jButton6ActionPerformed


